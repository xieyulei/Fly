package com.xyl.fly.fragment.animation.ofobject;

/**
 * Point
 *
 * @author xyl
 * @date 2021-08-25
 */
public class Point {
    private final float x;
    private final float y;

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
